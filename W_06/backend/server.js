const express = require('express');
const cors = require('cors');
const app = express();
const port = 3000;
const privateApi = require('./routes/private/apiProtectRoutes.js');
const auth = require('./routes/auth/apiAuthRoutes.js');

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

const allowedDomains = [
    'http://127.0.0.1:54636',
    'http://127.0.0.1:5050'
];

const corsOptions = {
    origin: function (origin, callback) {
        if (!origin) {
            return callback(new Error('Origin is missing'), false);
        }
        if (allowedDomains.includes(origin)) {
            callback(null, true); // อนุญาต
        } else {
            callback(new Error('Not allowed by CORS'));
        }
    },
    methods: ['GET'],
    credentials: true,
    allowedHeaders: ['Content-Type', 'Authorization']
};
app.use(cors(corsOptions));
app.use(express.json());
app.use((err, req, res, next) => {
    if (err.message === 'Not allowed by CORS') {
        res.status(403).json({ error: 'Access denied: CORS policy does not allow this origin' });
    } else {
        next(err);
    }
});

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

app.use('/api/auth', auth);
app.use('/api/private', privateApi);

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

app.listen(port, () => {
    console.log(`Server running at http://localhost:${port}`);
}); 
